package com.isaac.db;

import java.util.ArrayList;

public class DatabaseManager extends Database {

	// =============================================================================================
	// Constants
	// =============================================================================================

	// =============================================================================================
	// Fields
	// =============================================================================================
	
	public static DatabaseManager instance = null;
	
	// =============================================================================================
	// Constructors
	// =============================================================================================

	// =============================================================================================
	// Getter & Setter
	// =============================================================================================

	public static DatabaseManager getInstance() {
		if (instance == null)
			instance = new DatabaseManager();
		return instance;
	}
	
	// =============================================================================================
	// Methods for/from SuperClass/Interfaces
	// =============================================================================================

	// =============================================================================================
	// Methods
	// =============================================================================================

	public void save(ArrayList<Integer> score) {
		put("score_length", score.size());
		
		for (int i = 0; i < score.size(); i++) {
			put("score" + i, score.get(i));
		}
	}
	
	public void load(ArrayList<Integer> score) {
		int scoreLength = get("score_length", 0);
		
		for (int i = 0; i < scoreLength; i++) {
			score.add(get("score" + i, 0));
		}
	}
	
	// =============================================================================================
	// Inner and Anonymous Classes
	// =============================================================================================

}
